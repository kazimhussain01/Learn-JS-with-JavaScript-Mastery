//! Change String Case
console.log("====Change String Case====")

//! upperCase String
const mixedCaseString = "Hello! How Are You."
console.log(mixedCaseString) // Hello! How Are You.

console.log("====upperCase String====")
const UpperCase = mixedCaseString.toUpperCase()
console.log(UpperCase)

//! LowerCase String
console.log("====LowerCase String====")

const LowerCase = mixedCaseString.toLowerCase()
console.log(LowerCase)