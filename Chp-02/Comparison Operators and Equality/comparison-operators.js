//! Comparison Operator
//* The < (less than), > (greater than), <= (less than or equal), and >= (greater than or equal) comparison, also known as relational, operators compare their operands. Those operators are supported by all integral and floating-point numeric types.

//* Operator Description Comparing
// >	greater than    x > 8
// <	less than       x < 8
// >=	greater than or equal to    x >= 8
// <=	less than or equal to       x <= 8
// ==   Is Equal to
// (!=) Is Not Equal to

//! Greater than >
var a = 5;
var b = 10
console.log("Greater than:",a > b)

//! One More Example
var a = 20
var b = 10
console.log("Greater than:",a > b)

//! Less than <
var a = 5;
var b = 10
console.log("Less than:",a < b)

//! One More Example
var a = 20
var b = 10
console.log("Less than:",a < b)

//! Greater Equal to than >= 
var a = 5;
var b = 10
console.log("Greater than equal to:",a >= b)

//! One More Example 
var a = 20
var b = 10
console.log("Greater than equal to:",a >= b)

//! One More Example 
var a = 10
var b = 10
console.log("Greater than equal to:",a >= b)

//! Less Equal to than <= 
var a = 5;
var b = 10
console.log("Less than equal to:",a <= b)

//! One More Example 
var a = 20
var b = 10
console.log("Less than equal to:",a <= b)

//! One More Example 
var a = 10
var b = 10
console.log("Less than equal to:",a <= b)

//! Equal to Or Not Equal to

//! Is Equal to ==
var a = 20
var b = 30
console.log("Equal to:",a == b) 

//! One More Example
var b = 20; 
var c = 20;
console.log("Equal to:", b == c)

//! Is Not Equal to !=
var a = 20
var b = 30
console.log("Is not Equal to:",a != b) 

//! One More Example
var b = 20; 
var c = 20;
console.log("Is not Equal to:", b != c)