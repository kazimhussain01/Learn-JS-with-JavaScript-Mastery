//! Assignment Operator
//* The assignment operator = assigns the value of its right-hand operand to a variable, a property, or an indexer element given by its left-hand operand. The result of an assignment expression is the value assigned to the left-hand operand.

//! Assignment Operator
console.log("Assignment Operator")

//! 1st Example
var number = 5;
number += 5
console.log(number)

//! 2nd Example
var number = 5;
number -= 5
console.log(number)

//! 3rd Example
var number = 5;
number *= 5
console.log(number)

//! 4th Example
var number = 5;
number /= 5
console.log(number)

//! 5th Example
var String = "Hello"
String += ", I'm Kazim"
console.log(String) // "Hello, I'm Kazim"

//! 6th Example
var String = "Hello"
